class condition{//class name called condition
    public static void main(String arg[]){//main method
        int days=7;//initialization
        switch(days){//expression
            case 1:System.out.println("monday");//when you enter 1 the statment will display monday
            break;//break is optional
            case 2:System.out.println("thuesday");//when you enter 1 the statment will display thuesday
            break;//break is optional
            case 3:System.out.println("wednesday");//when you enter 1 the statment will display wednesday
            break;//break is optional
            case 4:System.out.println("thursday");//when you enter 1 the statment will display thursday
            break;//break is optional
            case 5:System.out.println("friday");//when you enter 1 the statment will display friday
            break;//break is optional
            case 6:System.out.println("saturday");//when you enter 1 the statment will display saturday
            default: // When none of the cases is true, a default statement is used.
            case 7:System.out.println("sunday");//when you enter 1 the statment will display sunday
        }
    }
}