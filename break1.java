class break1 {//class name
    public static void main(String[] args) {
        for (int i = 0; i <= 10; ++i) //initialization
        {  
            if (i==6)//if the value of i is 5 loop terminate
             {
                break;
            }      
            System.out.println(i);//this line will print value of i
        }   
    }
}